#[derive(Debug, Clone)]
struct RuntimeSpec {
    platform: &'static str,
    label: &'static str,
    accelerator: &'static str,
    backend: &'static str,
    library_name: &'static str,
    priority: i32,
    n_gpu_layers: i32,
}

#[derive(Debug, Default)]
struct HardwareProbe {
    cuda: bool,
    rocm: bool,
    metal: bool,
}

