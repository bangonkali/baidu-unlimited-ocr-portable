#[must_use]
pub(crate) fn find_model(model_id: &str) -> Option<&'static ModelCatalogEntry> {
    model_catalog()
        .iter()
        .chain(embedding_model_catalog().iter())
        .find(|entry| entry.model_id == model_id)
}
